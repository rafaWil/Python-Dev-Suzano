# CRIANDO SETS
"""
    >> Um set é uma coleção que não possui objetos repetidos, usamos sets para representar conjuntos matemáticos ou eliminar itens duplicados de um iterável.
"""

# Exemplo
set([1, 2, 3, 1, 3, 4]) # {1,2,3,4}

set("abacaxi")  # {"b", "a", "c", "x", "i"}


