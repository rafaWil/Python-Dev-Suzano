# DATE TIME?

"""
    É usado para lidar com datas e horas. Ele possui várias classes úteis como DATE, TIME e TIMEDELTA.
"""
# Exemplo
import datetime # Biblioteca para uso de data/hora

data = datetime.date(2022, 5, 17)
print(data) # => Resultado 2025-03-17

# retornando data atual 
data_atual = datetime.date.today()
print(data_atual) # => Resultado data atual