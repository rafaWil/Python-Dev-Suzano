# Maiuscula, Minuscula e Titulo

# Variavel com letras de tamanho diferente
curso = "pYtHon"

# Transformando em Maiuscula
print(curso.upper())
# >>> Resultado -> PYTHON

# Transformando em Minuscula
print(curso.lower())
# >>> Resultado -> python

# Transformando em Titulo
print(curso.title())
# >>> Resultado -> Python
