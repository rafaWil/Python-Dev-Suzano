# Variaveis com valores para exemplo
saldo = 200
saque = 200

# Print para teste de Op. Comparacao
print("Resultado se saldo é diferente de saque -> ", saldo != saque)
print("Resultado se saldo é igual a saque -> ", saldo == saque)
print("Resultado se saldo é maior que saque -> ", saldo  >= saque)
print("Resultado se saldo é menor que saque -> ", saldo <= saque)
